/*Program to perform arithemetic operations(+,-,*,/) on two given double type numbers 
and show the results */

public class arithemetic_operations {
    public static void main (String args[])
    {
        double x = 3.0;
        double y = 5.0;
        System.out.println("x+y=" + (x + y));
        System.out.println("x-y=" + (x - y));
        System.out.println("x/y=" + (x / y));
        System.out.println("x*y="+ (x*y));
    }
}
